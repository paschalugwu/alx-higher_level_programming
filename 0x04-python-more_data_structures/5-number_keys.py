#!/usr/bin/python3
"""
Let's begin by defining the function number_keys
with the parameter a_dictionary
"""


def number_keys(my_dictionary):
    """
    We are going to use len() function to calculate
    the number of keys in a_dictionary
    """
    num_keys = len(my_dictionary)
    """
    Finally, we return the value of num_keys
    """
    return num_keys
